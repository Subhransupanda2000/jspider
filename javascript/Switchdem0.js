var day=Number(prompt("enter day number"))
var res=day
switch(res)
{
    case 1:
    console.log("mon");break;
    case 2:
    console.log("tue");break;
    case 3:
    console.log("wed");break;
    case 4:
    console.log("thu");break;
    case 5:
    console.log("fri");break;
    case 6:
    console.log("sat");break;
    case 7:
    console.log("sun");break;
    default:
    console.log("put valid number");
}
