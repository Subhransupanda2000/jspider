var result=prompt("enter day name").toLowerCase();

var days=result;
switch(days)
{
    case 'sunday':
    console.log("day1");
    break;
    case 'monday':
    console.log("day2");
    break;
    case 'tuesday':
    console.log("day3");
    break;
    default:
    console.log("not found");
    case 'wednesday':
    console.log("day4");
    break;
    case 'thursday':
    console.log("day5");
    break;
    case 'saturday':
    console.log("day6");
    break;
    
}
